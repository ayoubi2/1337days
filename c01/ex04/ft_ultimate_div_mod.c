#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp_a;
	int	tmp_b;

	tmp_a = *a;
	tmp_b = *b;
	if (*b != 0)
	{
		*a = tmp_a / tmp_b;
		*b = tmp_a % tmp_b;
	}
}
