#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	j;

	a = 0;
	j = size - 1;
	while (a < j)
	{
		b = tab[a];
		tab[a] = tab[j];
		tab[j] = b;
		j--;
		a++;
	}
}
